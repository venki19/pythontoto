
# Append Function : Will append a entry in list.

Fruits = ["Banana", "Apple", "peach"]

Fruits.append("Orange")

print(Fruits)

# Output : ['Banana', 'Apple', 'peach', 'Orange']

# Length function for calculating no of items in the list (len) function

Fruits = ["Banana", "Apple", "peach"]

print(len(Fruits))

# output : 3

# Insert Function at a particular index with our values

Fruit = ["Apple", "Orange", "peach"]

Fruit.insert(1, "Banana")

print(Fruit)

# Output : ['Apple', 'Banana', 'Orange', 'peach']

# Index Function it will give you at which position the value is present

Fruit = ["Apple", "Orange", "peach", "Banana"]

print(Fruit.index("Orange"))

# output : 1




